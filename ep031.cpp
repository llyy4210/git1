#include<stdio.h>

int yb[8] = {1,2,5,10,20,50,100,200};
int a[201] = {0};


int main(){
    a[0] = 1;
    for(int i = 0; i < 8; i++){
        for(int j = yb[i]; j < 201; j++){
            a[j] += a[j - yb[i]]; 
        }
    }
printf("%d\n",a[200]);
    return 0;
}
